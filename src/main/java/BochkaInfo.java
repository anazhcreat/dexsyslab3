public class BochkaInfo {
    private String etiketka;
    private String info;

    public String getEtiketka() {
        return etiketka;
    }

    public String getInfo() {
        return info;
    }

    public BochkaInfo(String etiketka, String info) {
        this.etiketka = etiketka;
        this.info = info;
    }
}
